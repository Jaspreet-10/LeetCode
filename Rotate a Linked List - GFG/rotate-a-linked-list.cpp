class Solution
{
    public:
    //Function to rotate a linked list.
    Node* rotate(Node* head, int k)
    {
        vector<int>v1,v2;
        Node* h1=head;
        while(head!=NULL){
            v1.push_back(head->data);
            v2.push_back(head->data);
            head=head->next;
        }
        int n=v1.size();
        for(int i=0;i<n;++i){
            v1[(i+n-k)%n]=v2[i];
        }
        head=h1;
        int i=0;
        while(head!=NULL){
            head->data=v1[i];
            head=head->next;
            ++i;
        }
        return h1;
    }
};
    


