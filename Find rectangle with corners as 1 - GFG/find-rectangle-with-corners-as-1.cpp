class Solution{
    public:
    bool ValidCorner(const vector<vector<int> >& matrix)
    {
        unordered_map<int,int>m;
        int count=0;
        for(int i=0;i<matrix.size();++i){
            int row=-1;
            for(int j=0;j<matrix[i].size();++j){
                if(matrix[i][j]==1){
                    m[j]++;
                    if(m[j]==2)
                        count++;
                }
                    if(count==2)
                    return true;
            }
        }
        return false;
    }
};

